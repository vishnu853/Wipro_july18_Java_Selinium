package LabSession_July_22;

public class IfElseStatements
{
	public static void main(String[] args)
	{
		int num=-23;
		
		if(num<0)
		{
			System.out.println("Number is Negative");
		}
		
		if(num==0)
		{
			System.out.println("Number is zero");
		}
		
		if(num>0)
		{
			System.out.println("Number is positive");
		}
	}
}
