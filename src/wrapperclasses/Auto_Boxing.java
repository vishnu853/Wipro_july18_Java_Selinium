package wrapperclasses;

public class Auto_Boxing
{
	public static void main(String[] args) 
	{
		//Auto_Boxing
		
		int a=10;
		char ch='d';
		double d =2.9;
		float f1=8.0f;
		
		
		Integer i=a;
		System.out.println(i);
		
		Character ch1=ch;
		System.out.println(ch1);
		
		Double d1=d;
		System.out.println(d1);
		
		Float f2=f1;
		System.out.println(f2);
		
		
		
		
	}
}
